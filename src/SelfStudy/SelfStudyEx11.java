package SelfStudy;

public class SelfStudyEx11 {

	public static void main(String[] args) {

		
		int sum = 0;

		// 1씩증가 //sum = sum + 1;
		System.out.println(sum);    //증가 연산자가 없으니까 당연히 증가안됨
		sum++;
		System.out.println(sum);	//sum = 0에서 증가함
		
		int sum2 = 0 ;
		System.out.println(sum2);
		sum2--;
		System.out.println(sum2);
		sum2--;
		System.out.println(sum2);
	}

}
